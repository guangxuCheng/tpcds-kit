select  *
from (select avg(ss_list_price) B1_LP
            ,count(ss_list_price) B1_CNT
            ,count(distinct ss_list_price) B1_CNTD
      from store_sales
      where ss_quantity between 0 and 5
        and (ss_list_price between 62 and 62+10 
             or ss_coupon_amt between 15975 and 15975+1000
             or ss_wholesale_cost between 69 and 69+20)) B1,
     (select avg(ss_list_price) B2_LP
            ,count(ss_list_price) B2_CNT
            ,count(distinct ss_list_price) B2_CNTD
      from store_sales
      where ss_quantity between 6 and 10
        and (ss_list_price between 1 and 1+10
          or ss_coupon_amt between 9194 and 9194+1000
          or ss_wholesale_cost between 65 and 65+20)) B2,
     (select avg(ss_list_price) B3_LP
            ,count(ss_list_price) B3_CNT
            ,count(distinct ss_list_price) B3_CNTD
      from store_sales
      where ss_quantity between 11 and 15
        and (ss_list_price between 139 and 139+10
          or ss_coupon_amt between 17156 and 17156+1000
          or ss_wholesale_cost between 73 and 73+20)) B3,
     (select avg(ss_list_price) B4_LP
            ,count(ss_list_price) B4_CNT
            ,count(distinct ss_list_price) B4_CNTD
      from store_sales
      where ss_quantity between 16 and 20
        and (ss_list_price between 179 and 179+10
          or ss_coupon_amt between 1784 and 1784+1000
          or ss_wholesale_cost between 46 and 46+20)) B4,
     (select avg(ss_list_price) B5_LP
            ,count(ss_list_price) B5_CNT
            ,count(distinct ss_list_price) B5_CNTD
      from store_sales
      where ss_quantity between 21 and 25
        and (ss_list_price between 17 and 17+10
          or ss_coupon_amt between 8353 and 8353+1000
          or ss_wholesale_cost between 15 and 15+20)) B5,
     (select avg(ss_list_price) B6_LP
            ,count(ss_list_price) B6_CNT
            ,count(distinct ss_list_price) B6_CNTD
      from store_sales
      where ss_quantity between 26 and 30
        and (ss_list_price between 14 and 14+10
          or ss_coupon_amt between 3699 and 3699+1000
          or ss_wholesale_cost between 27 and 27+20)) B6
limit 100;

